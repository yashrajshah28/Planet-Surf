        <h1>Traveling Tips</h1>
        <p>We have put together a couple of simple general guidelines for you to follow, to make your trip to India a truly pleasant
            one. India is a land of diversities, quite different from any other country. Each state in India has its own distinct
            culture, language, traditions and food habits quite different from the rest of the states country – so it is advised that
            you read the following guidelines carefully to avoid any hassles or unwanted troubles along your journey.</p>
        <div class="info-list">
            <h2>Clothing & Accessories</h2>
            <ul>
                <li>Pack versatile clothing suitable for various climates.</li>
                <li>Include comfortable shoes, sunscreen, and a hat.</li>
                <li>Carry a travel-sized first aid kit.</li>
                <li>Bring a lightweight rain jacket or poncho.</li>
                <li>Consider packing a travel adapter for electronic devices.</li>
            </ul>
            <p>India due to its huge geographical size does not fit a particular climate zone. Generally speaking you must include
                cotton outfits, sunglasses, hats, mosquito repellent cream/lotion, shoes/ light sandals, moisturizing lotion and
                sun-block lotion mandatory, while planning your trip. If you are traveling to hill stations or the Himalayas do
                ensure that you carry woolen outfits with you. When in public places, care must be exercised to ensure your clothes
                are within the purview of decency to avoid unwanted attention.</p>
        </div>

        <div class="info-list">
            <h2>Health & Safety</h2>
            <ul>
                <li>Stay hydrated and drink bottled water in unfamiliar places.</li>
                <li>Research health risks and get necessary vaccinations.</li>
                <li>Keep important documents and valuables secure.</li>
                <li>Be cautious of your surroundings, especially in crowded areas.</li>
                <li>Have travel insurance to cover unexpected emergencies.</li>
            </ul>
            <p>Carry a bottle of mineral water with you at all times. Do ensure that the seal is not tampered with when buying
                mineral water bottles. Do not drink water from public taps or road-side vendors. If you feel like having a juice,
                please have it from a hygienic restaurant than from a road-side shack.</p>
        </div>

        <div class="info-list">
            <h2>Transportation</h2>
            <ul>
                <li>Plan transportation in advance and consider public transit options.</li>
                <li>Learn local traffic rules and customs.</li>
                <li>Keep emergency contact information handy.</li>
                <li>Use reputable taxi services or ride-sharing apps.</li>
                <li>Be aware of common scams and pickpocketing techniques.</li>
            </ul>
            <p>Buses are the cheapest option, auto-rickshaws, cabs and trains come next in the line. When traveling on a rickshaw
                or cab do ask the rates before you board the vehicle to avoid any problems and confusions. Also it is advisable to
                check the rates with some authority figure beforehand. When traveling in a train for long-distance reserve your
                tickets in advance, you can also get reserved tickets for short-distance which is the best way to avoid the heavy
                rush in un-reserved compartments. If you don’t get your tickets through the regular way you can get them through
                the “tatkal’ quota by paying a little extra charge. Do ensure to carry toilet paper with you when you are going on
                a long distance journey in train. Most of the train-stations have a pre-paid rickshaw counter, which is the best way
                to avoid bargaining and squabbling with the rickshaw driver.</p>
        </div>

        <div class="info-list">
            <h2>Smoking</h2>
            <p>This may come as a surprise to many; most places in India has a strict ban on smoking in public places. Smoking in
                public places is considered a punishable offence under law.</p>
        </div>

        <div class="info-list">
            <h2>Money</h2>
            <p>Foreign exchange including foreign currency, traveler’s cheques, and drafts drawn on banks in India or letters of
                credit can be brought into India without any limit. However, if the gross amount brought into India in any form
                at one time exceeds USD 10,000/- or its equivalent, it is required to be declared by the holder to the Indian customs
                authorities on arrival on a Currency Declaration Form. The forms of which are available with the airport/seaport
                customs department.</p>
        </div>

        <div class="info-list">
            <h2>Banks & Atms</h2>
            <p>The nationalized banks are open for business from 10.00 in the morning to 14.00 hrs in the afternoon. A couple of
                private banks are open for business between 8.00 in the morning to 8.00 in the night. There is also an extensive
                number of ATMs spread across the metros, towns and villages of India.</p>
        </div>

        <div class="info-list">
            <h2>Credit Cards</h2>
            <p>Most of the prominent hotels, shops and restaurants accept major credit cards.</p>
        </div>

        <div class="info-list">
            <h2>Drugs</h2>
            <p>Carrying or found possessing drugs is considered a grave criminal offence punishable under law.</p>
        </div>

        <div class="info-list">
            <h2>Temple Codes</h2>
            <p>If you are visiting a temple, please do ensure that you understand the rules and regulations concerning that
                particular temple before going in. Each temple has its own rules and codes of conduct that must be strictly
                adhered to. In general, non-Hindus are barred from entering most of the temples. Wearing footwear and particular
                type of clothes inside the temple premises are also strictly banned.</p>
        </div>

        <div class="info-list">
            <h2>Food</h2>
            <p>Do make it a point to savor some of the authentic Indian cuisines, garnished with exotic spices and herbs, while
                you are here. North Indian cuisines are quite different from their Southern and Eastern counterparts. In fact,
                each region has different types of cuisines and methods of cooking, quite distinct from the other. Do make it a
                point to savor the regional specialties when you travel.</p>
        </div>

        <div class="info-list">
            <h2>Medicines</h2>
            <p>Do carry your medical prescriptions and enough medicines to make your journey hassle-free. Also ensure that you have
                taken proper vaccinations and precautions before you enter the country.</p>
        </div>

        <div class="info-list">
            <h2>Electricity</h2>
            <p>The standard voltage of electric supply in the whole of India is between 220 – 240 Volts. Two and three pin plugs are
                widely used and you will have to get a voltage converter if the devices you carry do not accept 240 Volts at 50 Hertz.
                Voltage converters are easily available in the electrical stores. The voltage converters that are adaptable to the
                Indian voltage are Resistor Network Converters (50-1600 Watts), Transformers (50-1000 Watts) and Combination Converters.</p>
        </div>

        <div class="info-list">
            <h2>Cultural Etiquette</h2>
            <ul>
                <li>Respect local customs, traditions, and dress codes.</li>
                <li>Learn basic phrases in the local language.</li>
                <li>Be mindful of cultural taboos and behaviors.</li>
                <li>Ask for permission before taking photos of locals or religious sites.</li>
                <li>Remove shoes before entering temples or homes in some cultures.</li>
            </ul>
        </div>

        <div class="info-list">
            <h2>Pre-departure tips</h2>
            <ul>
                <li>Pack your bags in advance.</li>
                <li>Keep print out of tour plan and Hotel confirmation vouchers.</li>
                <li>Carry the valid identify proofs (driver license/ Voter card/ Passport).</li>
                <li>Personal Medicines, Sunscreen lotion, Lip balm, Glucose.</li>
                <li>Chocolates, biscuits, nuts and mineral water.</li>
                <li>Other essentials: Paper soap, toothbrush, toothpaste, comb, Slippers and toilet paper</li>
                <li>Sweater, Socks. Handkerchief/towels and sports shoes as per weather conditions</li>
                <li>Chargers for all the gadgets, cameras and mobile</li>
                <li>3 socket power strip</li>
                <li>Enough memory cards to last you the entire trip</li>
            </ul>
